# dictioniary
# just like key value pair
myDict = {

    "Yash": "In a Quick Manner",
    "Punnet": "Wow ",
    "Marks": [1, 2, 4],
    "anotherDict": {"Name": "Yash"}  # nested dictioniary
}

print(myDict['Yash'])
print(myDict["Marks"])
print(myDict["anotherDict"]["Name"])

# we can update dictioniary data 
myDict["Marks"]=[100,200,100022]

print(myDict["Marks"])
